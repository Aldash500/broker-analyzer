
import streamlit as st
from datetime import datetime

st.title("Брокерский отчёт по выбранному периоду")

# Выбор года
year = st.selectbox("Выберите год", list(range(2020, datetime.now().year + 1))[::-1], index=1)

# Выбор месяцев
months_ru = {
    "Январь": "01", "Февраль": "02", "Март": "03",
    "Апрель": "04", "Май": "05", "Июнь": "06",
    "Июль": "07", "Август": "08", "Сентябрь": "09",
    "Октябрь": "10", "Ноябрь": "11", "Декабрь": "12"
}
selected_months = st.multiselect("Выберите месяцы", list(months_ru.keys()), default=[
    "Апрель", "Май", "Июнь", "Июль", "Август", "Сентябрь", "Октябрь", "Ноябрь", "Декабрь"
])

if st.button("Сформировать отчёт"):
    st.success(f"Идёт формирование отчёта за {', '.join(selected_months)} {year}...")
    st.info("(Функция генерации отчёта будет добавлена в полной версии.)")
